  soup = BeautifulSoup(html_doc, 'html.parser')
  hrefs = [i.get('href') for i in soup.find_all('a') if 'pages-meta-current' in i.get('href')]
  if len(hrefs) > 1:
    for i in hrefs[1:]:
      yield i
  else:
    yield hrefs[0]

